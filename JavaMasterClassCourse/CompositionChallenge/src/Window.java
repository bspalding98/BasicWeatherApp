public class Window {
    private int height;
    private int length;
    private int panels;

    public Window(int height, int length, int panels) {
        this.height = height;
        this.length = length;
        this.panels = panels;
    }

//    getters
    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }

    public int getPanels() {
        return panels;
    }
}
